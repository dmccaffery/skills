"""Generate EVALUATION.md reports from evals-results/*.json (schema 2).

Two levels, both organised as one section per model provider (Anthropic,
OpenAI, Google) with one row/subsection per model:

- EVALUATION.md (repo root)            — plugin-level rollup, one triggers
  table and one cases table per plugin: pass rates, total input tokens,
  estimated input cost, and (cases) the measured usage of executed runs.
- plugins/<plugin>/EVALUATION.md       — per-eval detail: every trigger query
  and behavioral case with its result, token count, and cost; case tables
  also total the measured usage per model.

evals-results/ is gitignored (raw run data stays local); the EVALUATION.md
files are the committed record. run_triggers.py and run_cases.py call
generate() automatically after every run.

Usage:
  python3 tools/eval/report.py
"""

import json

import providers

REPO = providers.REPO
RESULTS_DIR = providers.RESULTS_DIR

GENERATED = "<!-- Generated by tools/eval/report.py from evals-results/ — do not edit by hand. -->"

METHODOLOGY = """\
Token usage is measured with each provider's official token-counting API — never a local
tokenizer. Per eval, the counted payload is the skill's `SKILL.md` plus the eval prompt (the
marginal context a triggering eval loads), priced at the model's published input rate:

- Anthropic: [`POST /v1/messages/count_tokens`](https://platform.claude.com/docs/en/build-with-claude/token-counting)
- OpenAI: [`POST /v1/responses/input_tokens`](https://developers.openai.com/api/docs/guides/token-counting)
- Google: [`models.countTokens`](https://ai.google.dev/gemini-api/docs/tokens)

Cells show an em dash when a model has not been evaluated or counted yet, and `n/a` where the
provider has not published pricing. Behavioral (Tier 2) runs additionally record the
harness-reported usage of the live session — total input tokens (including cache writes and
reads), output tokens, and cost.\
"""

REGENERATE = """\
## Regenerating

Trigger and behavioral evals run real agent sessions (Anthropic via `claude`, OpenAI via
`codex`, Google via `gemini` when available); token counting only needs provider credentials:
`ANTHROPIC_API_KEY` (or a long-lived `CLAUDE_CODE_OAUTH_TOKEN` from `claude setup-token`),
`OPENAI_API_KEY`, and `GEMINI_API_KEY`.

```sh
python3 tools/eval/run_triggers.py --models all   # Tier 1 + token usage, all providers
python3 tools/eval/run_cases.py --models all      # Tier 2 + token usage, all providers
python3 tools/eval/run_triggers.py --count-only --models all  # token usage only, no sessions
python3 tools/eval/report.py                      # rebuild EVALUATION.md files only
```\
"""


def dash(value):
    return "—" if value is None else value


def fmt_tokens(tokens):
    return "—" if tokens is None else f"{tokens:,}"


def fmt_cost(cost, tokens, model):
    if tokens is not None and model["input"] is None:
        return "n/a"
    return "—" if cost is None else f"${cost:.4f}"


def fmt_frac(passed, total):
    return "—" if passed is None else f"{passed}/{total}"


def fmt_measured_tokens(input_tokens, output_tokens):
    return "—" if input_tokens is None else f"{input_tokens:,}/{output_tokens or 0:,}"


def fmt_usd(cost):
    return "—" if cost is None else f"${cost:.4f}"


def cell(text, limit=70):
    text = " ".join(str(text).split()).replace("|", "\\|")
    return text if len(text) <= limit else text[: limit - 1] + "…"


def load(kind, skill):
    path = RESULTS_DIR / f"{kind}-{skill}.json"
    if path.is_file():
        try:
            data = json.loads(path.read_text())
            if isinstance(data, dict) and data.get("schema") == 2:
                return data
        except json.JSONDecodeError:
            pass
    return None


def plugin_layout():
    """[(plugin, trigger_skills, case_skills)] discovered from plugins/*/evals."""
    layout = []
    for evals_dir in sorted(REPO.glob("plugins/*/evals")):
        plugin = evals_dir.parent.name
        triggers = sorted(p.parent.name for p in evals_dir.glob("*/triggers.json"))
        cases = sorted(p.parent.name for p in evals_dir.glob("*/cases.json"))
        layout.append((plugin, triggers, cases))
    return layout


def model_entry(kind, skill, model_id):
    data = load(kind, skill)
    return (data or {}).get("models", {}).get(model_id)


def rollup(kind, skills, model_id):
    """Aggregate (passed, total, tokens, cost) across skills for one model."""
    passed = total = tokens = cost = None
    for skill in skills:
        entry = model_entry(kind, skill, model_id)
        if not entry:
            continue
        summary = entry["summary"]
        if summary["passed"] is not None:
            passed = (passed or 0) + summary["passed"]
            total = (total or 0) + summary["total"]
        if summary["input_tokens"] is not None:
            tokens = (tokens or 0) + summary["input_tokens"]
        if summary["est_input_cost_usd"] is not None:
            cost = (cost or 0) + summary["est_input_cost_usd"]
    return passed, total, tokens, cost


def rollup_measured(skills, model_id):
    """Aggregate measured case usage (input, output, cost) across skills."""
    tokens_in = tokens_out = cost = None
    for skill in skills:
        entry = model_entry("cases", skill, model_id)
        summary = (entry or {}).get("summary", {})
        if summary.get("measured_input_tokens") is not None:
            tokens_in = (tokens_in or 0) + summary["measured_input_tokens"]
        if summary.get("measured_output_tokens") is not None:
            tokens_out = (tokens_out or 0) + summary["measured_output_tokens"]
        if summary.get("measured_cost_usd") is not None:
            cost = (cost or 0) + summary["measured_cost_usd"]
    return tokens_in, tokens_out, None if cost is None else round(cost, 4)


def root_report(layout):
    lines = [
        "# Evaluation results",
        "",
        GENERATED,
        "",
        "Plugin-level eval results and token cost per provider model. Per-eval detail lives in",
        "each plugin's own `EVALUATION.md` (for instance `plugins/terraform/EVALUATION.md`).",
        "",
        METHODOLOGY,
        "",
    ]
    for provider in providers.PROVIDERS.values():
        lines += [f"## {provider['display']}", ""]
        for plugin, trigger_skills, case_skills in layout:
            lines += [
                f"### {plugin}",
                "",
                "#### Triggers",
                "",
                "| Model | Passed | Input tokens | Est. input cost (USD) |",
                "| --- | --- | --- | --- |",
            ]
            for model in provider["models"]:
                passed, total, tokens, cost = rollup("triggers", trigger_skills, model["id"])
                lines.append(
                    f"| {model['display']} (`{model['id']}`) "
                    f"| {fmt_frac(passed, total)} "
                    f"| {fmt_tokens(tokens)} | {fmt_cost(cost, tokens, model)} |"
                )
            lines += [
                "",
                "#### Cases",
                "",
                "| Model | Passed | Input tokens | Est. input cost (USD) "
                "| Measured tokens (in/out) | Measured cost (USD) |",
                "| --- | --- | --- | --- | --- | --- |",
            ]
            for model in provider["models"]:
                passed, total, tokens, cost = rollup("cases", case_skills, model["id"])
                m_in, m_out, m_cost = rollup_measured(case_skills, model["id"])
                lines.append(
                    f"| {model['display']} (`{model['id']}`) "
                    f"| {fmt_frac(passed, total)} "
                    f"| {fmt_tokens(tokens)} | {fmt_cost(cost, tokens, model)} "
                    f"| {fmt_measured_tokens(m_in, m_out)} | {fmt_usd(m_cost)} |"
                )
            lines.append("")
    lines += [REGENERATE, ""]
    return "\n".join(lines)


def trigger_table(skill, entry, model):
    lines = [
        f"#### Triggers — {skill}",
        "",
        _run_note(entry),
        "",
        "| Query | Expected | Rate | Result | Input tokens | Est. input cost (USD) |",
        "| --- | --- | --- | --- | --- | --- |",
    ]
    for r in entry["results"]:
        rate = "—" if r["trigger_rate"] is None else f"{r['trigger_rate']:.2f}"
        result = "—" if r["passed"] is None else ("PASS" if r["passed"] else "FAIL")
        lines.append(
            f"| {cell(r['query'])} | {'yes' if r['should_trigger'] else 'no'} "
            f"| {rate} | {result} | {fmt_tokens(r['input_tokens'])} "
            f"| {fmt_cost(r['est_input_cost_usd'], r['input_tokens'], model)} |"
        )
    summary = entry["summary"]
    lines.append(
        f"| **Total** | | | **{fmt_frac(summary['passed'], summary['total'])}** "
        f"| **{fmt_tokens(summary['input_tokens'])}** "
        f"| **{fmt_cost(summary['est_input_cost_usd'], summary['input_tokens'], model)}** |"
    )
    lines.append("")
    return lines


def case_table(skill, entry, model):
    lines = [
        f"#### Cases — {skill}",
        "",
        _run_note(entry),
        "",
        "| Case | Result | Input tokens | Est. input cost (USD) "
        "| Measured tokens (in/out) | Measured cost (USD) |",
        "| --- | --- | --- | --- | --- | --- |",
    ]
    for r in entry["results"]:
        result = "—" if r["passed"] is None else ("PASS" if r["passed"] else "FAIL")
        measured = r.get("measured") or {}
        lines.append(
            f"| {cell(r['id'])} | {result} | {fmt_tokens(r['input_tokens'])} "
            f"| {fmt_cost(r['est_input_cost_usd'], r['input_tokens'], model)} "
            f"| {fmt_measured_tokens(measured.get('input_tokens'), measured.get('output_tokens'))} "
            f"| {fmt_usd(measured.get('cost_usd'))} |"
        )
    summary = entry["summary"]
    lines.append(
        f"| **Total** | **{fmt_frac(summary['passed'], summary['total'])}** "
        f"| **{fmt_tokens(summary['input_tokens'])}** "
        f"| **{fmt_cost(summary['est_input_cost_usd'], summary['input_tokens'], model)}** "
        f"| **{fmt_measured_tokens(summary.get('measured_input_tokens'), summary.get('measured_output_tokens'))}** "
        f"| **{fmt_usd(summary.get('measured_cost_usd'))}** |"
    )
    lines.append("")
    return lines


def _run_note(entry):
    if not entry.get("executed"):
        return f"Token counts only (no eval session executed). Counted {entry['ran_at']}."
    runs = entry.get("runs_per_query")
    suffix = f", {runs} runs per query" if runs else ""
    return f"Last run {entry['ran_at']}{suffix}."


def plugin_report(plugin, trigger_skills, case_skills):
    lines = [
        f"# {plugin} — evaluation results",
        "",
        GENERATED,
        "",
        "Per-eval results and token cost for every provider model. The plugin-level rollup",
        "lives in the repo root [`EVALUATION.md`](../../EVALUATION.md).",
        "",
        METHODOLOGY,
        "",
    ]
    for provider in providers.PROVIDERS.values():
        lines += [f"## {provider['display']}", ""]
        for model in provider["models"]:
            lines += [f"### {model['display']} (`{model['id']}`)", ""]
            blocks = []
            for skill in trigger_skills:
                entry = model_entry("triggers", skill, model["id"])
                if entry:
                    blocks += trigger_table(skill, entry, model)
            for skill in case_skills:
                entry = model_entry("cases", skill, model["id"])
                if entry:
                    blocks += case_table(skill, entry, model)
            if blocks:
                lines += blocks
            else:
                lines += ["No results recorded for this model yet — see [Regenerating](#regenerating).", ""]
    lines += [REGENERATE, ""]
    return "\n".join(lines)


def generate():
    layout = plugin_layout()
    written = []

    root = REPO / "EVALUATION.md"
    root.write_text(root_report(layout))
    written.append(root)

    for plugin, trigger_skills, case_skills in layout:
        path = REPO / "plugins" / plugin / "EVALUATION.md"
        path.write_text(plugin_report(plugin, trigger_skills, case_skills))
        written.append(path)

    for path in written:
        print(f"wrote {path.relative_to(REPO)}")
    return written


if __name__ == "__main__":
    generate()
